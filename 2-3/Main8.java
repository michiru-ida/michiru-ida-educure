/**自己紹介プログラムを作成してみましょう
*クラス名：Main8
*概要：ご自身の名前、出身地、好きな食べ物を表示させましょう
*/
public class Main8 {
//ここからメインメソッド
    public static void main(String[] args) {
        //名前を入れる箱
        String name ="山田太郎";
        //出身地を入れる箱 
        String where ="東京都";
        //好きな食べ物を入れる箱
        String food ="カレーライス";
        //""===== 自己紹介 ====="   と区切り線を表示させてください
        System.out.println("===== 自己紹介 =====");
        System.out.println("名前："+name);
        System.out.println("出身地："+where); 
        System.out.println("好きな食べ物："+food); 
    }

    
}